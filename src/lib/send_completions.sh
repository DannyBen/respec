## [@bashly-upgrade completions send_completions]
send_completions() {
  echo $'# respec completion                                        -*- shell-script -*-'
  echo $''
  echo $'# This bash completions script was generated by'
  echo $'# completely (https://github.com/bashly-framework/completely)'
  echo $'# Modifying it manually is not recommended'
  echo $''
  echo $'_respec_completions_filter() {'
  echo $'  local words="$1"'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local result=()'
  echo $''
  echo $'  # words the user already typed (excluding the command itself)'
  echo $'  local used=()'
  echo $'  if ((COMP_CWORD > 1)); then'
  echo $'    used=("${COMP_WORDS[@]:1:$((COMP_CWORD - 1))}")'
  echo $'  fi'
  echo $''
  echo $'  if [[ "${cur:0:1}" == "-" ]]; then'
  echo $'    # Completing an option: offer everything (including options)'
  echo $'    echo "$words"'
  echo $''
  echo $'  else'
  echo $'    # Completing a non-option: offer only non-options,'
  echo $'    # and don\'t re-offer ones already used earlier in the line.'
  echo $'    for word in $words; do'
  echo $'      [[ "${word:0:1}" == "-" ]] && continue'
  echo $''
  echo $'      local seen=0'
  echo $'      for u in "${used[@]}"; do'
  echo $'        if [[ "$u" == "$word" ]]; then'
  echo $'          seen=1'
  echo $'          break'
  echo $'        fi'
  echo $'      done'
  echo $'      ((!seen)) && result+=("$word")'
  echo $'    done'
  echo $''
  echo $'    echo "${result[*]}"'
  echo $'  fi'
  echo $'}'
  echo $''
  echo $'_respec_completions() {'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local compwords=()'
  echo $'  if ((COMP_CWORD > 0)); then'
  echo $'    compwords=("${COMP_WORDS[@]:1:$((COMP_CWORD - 1))}")'
  echo $'  fi'
  echo $'  local compline="${compwords[*]}"'
  echo $''
  echo $'  COMPREPLY=()'
  echo $''
  echo $'  case "$compline" in'
  echo $'    \'completions\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'refactor\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'continue\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'failures\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'except\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'tagged\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'reset\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'focus\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'fails\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'cont\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'only\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'last\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'tags\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'tag\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'all\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'ex\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'t\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'l\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'e\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'o\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'c\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'f\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'a\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --repeat --watch -h -r -w")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    \'r\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_respec_completions_filter "--help --version -h -v a all c completions cont continue e ex except f fails failures focus l last o only r refactor reset t tag tagged tags")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'  esac'
  echo $'} &&'
  echo $'  complete -F _respec_completions respec'
  echo $''
  echo $'# ex: filetype=sh'
}